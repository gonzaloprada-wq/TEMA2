package buclefor;

public class Ejercicio4 {

	public static void main(String[] args) {

		// Creo una const
		final int DIEZ = 10;

		// creo un for que empieza en 1 que funciona del 1 hasta el 10 y va de 2 en dos
		for (int contador = 1; contador <= DIEZ; contador += 2) {
			
			//Imprimo el contador		
			System.out.println(contador);
		}
	};
}
